  /// يحوّل شكل العرض أو الحرف المنطقي إلى حرف عربي منطقي.
  static int? toLogicalRune(int rune) {
    if (rune >= 0x0600 && rune <= 0x06FF) return rune;
    return _presentationToLogical[rune];
  }

  static int encodeRune(int rune) {
    if (rune <= 0x7F) return rune;
    final logical = toLogicalRune(rune) ?? rune;
    return _logicalToByte[logical] ?? 0x3F;
  }
